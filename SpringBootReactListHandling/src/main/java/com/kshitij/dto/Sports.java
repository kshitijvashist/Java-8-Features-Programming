package com.kshitij.dto;

public class Sports {
	private int sportsNo;
	private String sportsName;
	private int sportslength;
	public int getSportsNo() {
		return sportsNo;
	}
	public void setSportsNo(int sportsNo) {
		this.sportsNo = sportsNo;
	}
	public String getSportsName() {
		return sportsName;
	}
	public void setSportsName(String sportsName) {
		this.sportsName = sportsName;
	}
	public int getSportslength() {
		return sportslength;
	}
	public void setSportslength(int sportslength) {
		this.sportslength = sportslength;
	}
	

}
